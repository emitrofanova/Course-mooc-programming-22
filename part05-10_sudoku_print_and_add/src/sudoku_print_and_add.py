def print_sudoku(sudoku: list):
    cnt = 0
    for i in sudoku:
        cnt += 1
        for j in range(9):
            if i[j] == 0:
                print("_", end=" ")
            else:
                print(i[j], end= " ")
            if j == 2 or j == 5:
                print(" ", end="")
        print()
        if cnt == 3 or cnt == 6:
            print()

def add_number(sudoku: list, row_no: int, column_no: int, number:int):
    sudoku[row_no][column_no] = number

